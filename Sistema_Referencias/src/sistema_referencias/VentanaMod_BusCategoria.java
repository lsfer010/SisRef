    private void jTCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTCategoriaMouseClicked
        PreparedStatement ps = null;
        ResultSet rs = null;
        try {
            Conexion connection = new Conexion();
            Connection con = connection.getConexion();
            
            int Fila = jTCategoria.getSelectedRow();
            String codigo = jTCategoria.getValueAt(Fila, 0).toString();
            
            ps = con.prepareStatement("SELECT id_categoria, nombre, descripcion FROM categoria WHERE id_categoria = ?");
            ps.setInt(1, Integer.parseInt(codigo));
            rs = ps.executeQuery();
            
            while (rs.next()) {
                tIDCategoria.setText(rs.getString("id_categoria"));
                tNombre.setText(rs.getString("nombre"));
                taDescripcion.setText(rs.getString("descripcion"));
            }
            
            con.close();
        } catch (SQLException ex) {
            JOptionPane.showInputDialog("Ocurrio un error: " + ex.toString());
        }
    }//GEN-LAST:event_jTCategoriaMouseClicked

    private void bModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bModificarActionPerformed
        if (!tIDCategoria.getText().equals("")) {
            Conexion connection = new Conexion();
            Connection con = connection.getConexion();
            PreparedStatement ps = null;
            
            try {
                ps = con.prepareStatement("UPDATE categoria SET nombre=?, descripcion=? WHERE id_categoria=?");
                ps.setString(1, tNombre.getText());
                ps.setString(2, taDescripcion.getText());
                ps.setInt(3, Integer.parseInt(tIDCategoria.getText()));
                
                int res = ps.executeUpdate();
                
                if(res > 0) {
                    JOptionPane.showInputDialog("Categoria modificada");
                } else {
                    JOptionPane.showInputDialog("Ocurrio un error al modificar");
                }
                
                con.close();
            } catch (Exception e) {
                JOptionPane.showInputDialog("Error: " + e.toString());
            }
        } else {
            JOptionPane.showInputDialog("Seleccione un elemento de la tabla para poder realizar el cambio");
        }
        
        actualizarTabla();
    }//GEN-LAST:event_bModificarActionPerformed

